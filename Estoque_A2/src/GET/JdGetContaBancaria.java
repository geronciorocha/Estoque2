    private void AlimentaTabela(List<ContaBancaria> listContaBancaria){
        if (listContaBancaria != null) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(new Vector(0));
            for (int i = 0; i < listContaBancaria.size(); i++) {
                model.addRow(new Vector(listContaBancaria));
                jTable1.setModel(model);
            }
        }
    }
    
    private void SetContaBancaria(){
        if(jTable1.getSelectedRow()>-1){
            GetContaBancaria.contaBancaria = (ContaBancaria) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
        }else{
            JOptionPane.showMessageDialog(this, "Selecione uma linha da tabela", "", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
